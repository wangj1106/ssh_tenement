package com.tenements.domain;

public class Fee {
	private Integer fid;
	private double wfee;
	private double efee;
	private double tfee;
	public Integer getFid() {
		return fid;
	}
	public void setFid(Integer fid) {
		this.fid = fid;
	}
	public double getWfee() {
		return wfee;
	}
	public void setWfee(double wfee) {
		this.wfee = wfee;
	}
	public double getEfee() {
		return efee;
	}
	public void setEfee(double efee) {
		this.efee = efee;
	}
	public double getTfee() {
		return tfee;
	}
	public void setTfee(double tfee) {
		this.tfee = tfee;
	}
}
